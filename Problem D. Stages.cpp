#include<bits/stdc++.h>
using namespace std;
char str[10001];

int main()
{
   ios_base :: sync_with_stdio(false);
   cin.tie(NULL);

   int n,k;
   cin>>n>>k;
   cin>>str;
   sort(str,str+n);
   int sum = str[0]-'a'+1;
   char temp = str[0];
   int num=1;
   for(int i=0;i<n;i++)
   {

       if(num == k)
        break;
       if(str[i]-temp>1)
       {

           num++;
           temp = str[i];
           sum = sum+temp-'a'+1;

       }
   }
   if(num == k)
   {
       cout <<sum<<endl;
   }
   else{
    cout<<-1<<endl;
   }
   return 0;
}

